"""初期学習用コーパス投入スクリプト。

何も学習していない状態だとマルコフは無言になりがちなので、
最初のひと盛りを食わせて"喋れる"状態にしておく。
    python seed_corpus.py
本番では、常駐中にユーザーの発言を食って勝手に育っていく。
"""
from brain.engine import Engine

# キレ系の口調に寄せた種データ。ここを増やすほど初期の語彙が豊かになる。
CORPUS = [
    "は？それ本気で言ってるわけ",
    "知らんけど、たぶん無理でしょ",
    "で、結局なにがしたいの",
    "そんなの自分で調べればいいじゃん",
    "めんどくさいから後にして",
    "また同じこと言ってる、学習しなよ",
    "別に興味ないんだけど",
    "へー、すごいすごい、で？",
    "そういうとこだぞ、ほんと",
    "眠いし腹減ったし、もう無理",
    "ていうか、それ昨日も聞いた",
    "正直どうでもいいかな",
    "がんばってるアピールいらないから",
    "まあ、好きにすれば",
    "うるさいな、ちょっと黙ってて",
    "今度こそ本気出すって何回目",
    "現実見たほうがいいと思うけど",
    "そんな顔してもなにも出ないよ",
    "暇なら掃除でもしてれば",
    "結論から言って、それはダサい",
]


def main():
    engine = Engine()
    for line in CORPUS:
        engine.markov.learn(line)
    print(f"投入完了。語彙 {engine.vocab_size}")


if __name__ == "__main__":
    main()
